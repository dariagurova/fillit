#include "libft.h"

static int	ft_blank(char c)
{
	return (c == ' ' || c == '\n' || c == '\t');
}

int			ft_spaces(char c)
{
	return (ft_blank(c) || c == '\f' || c == '\v' || c == '\r');
}
