#include "libft.h"

char	*ft_strncpy(char *dest, const char *src, size_t n)
{
	char		*ret;
	size_t		i;

	ret = dest;
	i = 0;
	while (src[i] != '\0' && i < n)
	{
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (ret);
}
